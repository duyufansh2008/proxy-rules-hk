const RAW_BASE = 'https://raw.githubusercontent.com/duyufansh2008/proxy-rules-hk/main';
const B7_BASE = 'https://raw.githubusercontent.com/blackmatrix7/ios_rule_script/master/rule';

const clients = {
  surge: { label: 'Surge', ext: 'conf', b7: 'Surge' },
  clash: { label: 'Clash / Mihomo', ext: 'yaml', b7: 'Clash' },
  loon: { label: 'Loon', ext: 'conf', b7: 'Loon' },
  quantumultx: { label: 'Quantumult X', ext: 'conf', b7: 'QuantumultX' },
  shadowrocket: { label: 'Shadowrocket', ext: 'conf', b7: 'Shadowrocket' }
};

const regions = {
  mainland: {
    label: '中国大陆',
    description: '白名单模式：中国大陆常用站点直连，其他流量走代理。',
    final: 'PROXY'
  },
  restricted: {
    label: '香港 / 澳门 / 其他受区域限制地区',
    description: '限制服务代理模式：仅 AI、TikTok 等服务走代理，其他流量直连。',
    final: 'DIRECT'
  }
};

const state = { client: 'surge', region: 'mainland', adblock: false };

function localRule(client, type) {
  if (client === 'surge') return `${RAW_BASE}/surge/ruleset/${type}.list`;
  if (client === 'clash') return `${RAW_BASE}/clash/ruleset/${type}.yaml`;
  if (client === 'loon') return `${RAW_BASE}/loon/ruleset/${type}.list`;
  if (client === 'quantumultx') return `${RAW_BASE}/quantumult-x/filter/${type}.list`;
  return `${RAW_BASE}/surge/ruleset/${type}.list`;
}

function b7Rule(client, category) {
  const folder = clients[client].b7;
  return `${B7_BASE}/${folder}/${category}/${category}.list`;
}

function commonHeader() {
  const region = regions[state.region];
  return [
    '# Generated by proxy-rules-hk',
    `# Client: ${clients[state.client].label}`,
    `# Region mode: ${region.label}`,
    '# Replace the Proxy policy/group with your real policy group or node.',
    ''
  ].join('\n');
}

function buildSurgeLikeConfig(client) {
  const lines = [commonHeader(), '[General]', 'loglevel = notify', '', '[Proxy Group]', 'Proxy = select, DIRECT', '', '[Rule]'];
  if (state.adblock) lines.push(`RULE-SET,${b7Rule(client, 'Advertising')},REJECT`);
  if (state.region === 'mainland') {
    lines.push(`RULE-SET,${b7Rule(client, 'ChinaMax')},DIRECT`);
    lines.push('FINAL,Proxy');
  } else {
    lines.push(`RULE-SET,${localRule(client, 'proxy')},Proxy`);
    lines.push('FINAL,DIRECT');
  }
  return `${lines.join('\n')}\n`;
}

function buildClashConfig() {
  const providers = [];
  const rules = [];
  if (state.adblock) {
    providers.push(['advertising', b7Rule('clash', 'Advertising')]);
    rules.push('  - RULE-SET,advertising,REJECT');
  }
  if (state.region === 'mainland') {
    providers.push(['china_max', b7Rule('clash', 'ChinaMax')]);
    rules.push('  - RULE-SET,china_max,DIRECT');
    rules.push('  - MATCH,Proxy');
  } else {
    providers.push(['restricted_services', localRule('clash', 'proxy')]);
    rules.push('  - RULE-SET,restricted_services,Proxy');
    rules.push('  - MATCH,DIRECT');
  }

  const providerYaml = providers.map(([name, url]) => `  ${name}:\n    type: http\n    behavior: classical\n    path: ./ruleset/${name}.yaml\n    url: ${url}\n    interval: 86400`).join('\n');
  return `${commonHeader()}proxy-groups:\n  - name: Proxy\n    type: select\n    proxies:\n      - DIRECT\n\nrule-providers:\n${providerYaml}\n\nrules:\n${rules.join('\n')}\n`;
}

function buildQuantumultXConfig() {
  const lines = [commonHeader(), '[policy]', 'static=Proxy, direct', '', '[filter_remote]'];
  if (state.adblock) lines.push(`${b7Rule('quantumultx', 'Advertising')}, tag=Advertising, force-policy=reject, enabled=true`);
  if (state.region === 'mainland') {
    lines.push(`${b7Rule('quantumultx', 'ChinaMax')}, tag=ChinaMax Direct, force-policy=direct, enabled=true`);
    lines.push('', '[filter_local]', 'final, Proxy');
  } else {
    lines.push(`${localRule('quantumultx', 'proxy')}, tag=Restricted Services, enabled=true`);
    lines.push('', '[filter_local]', 'final, direct');
  }
  return `${lines.join('\n')}\n`;
}

function buildConfig() {
  if (state.client === 'clash') return buildClashConfig();
  if (state.client === 'quantumultx') return buildQuantumultXConfig();
  if (state.client === 'shadowrocket') return buildSurgeLikeConfig('shadowrocket');
  return buildSurgeLikeConfig(state.client);
}

function fileName() {
  const ad = state.adblock ? '-adblock' : '';
  return `proxy-rules-${state.client}-${state.region}${ad}.${clients[state.client].ext}`;
}

function renderChoices() {
  const clientRoot = document.getElementById('clientChoices');
  clientRoot.innerHTML = Object.entries(clients).map(([key, item]) => `
    <button class="choice ${state.client === key ? 'active' : ''}" data-client="${key}" type="button">
      <strong>${item.label}</strong><span>${item.ext.toUpperCase()} 配置</span>
    </button>
  `).join('');

  const regionRoot = document.getElementById('regionChoices');
  regionRoot.innerHTML = Object.entries(regions).map(([key, item]) => `
    <button class="choice ${state.region === key ? 'active' : ''}" data-region="${key}" type="button">
      <strong>${item.label}</strong><span>${item.description}</span>
    </button>
  `).join('');
}

function renderConfig() {
  const config = buildConfig();
  const dataUrl = `data:text/plain;charset=utf-8,${encodeURIComponent(config)}`;
  const downloadLink = document.getElementById('downloadLink');
  downloadLink.href = dataUrl;
  downloadLink.download = fileName();
  document.getElementById('configPreview').value = config;
  document.getElementById('resultTitle').textContent = `${clients[state.client].label} · ${regions[state.region].label} · ${state.adblock ? '开启广告拦截' : '无广告拦截'}`;
  document.getElementById('resultDescription').textContent = regions[state.region].description;
}

async function copyText(text, label) {
  await navigator.clipboard.writeText(text);
  const original = label.textContent;
  label.textContent = '已复制';
  setTimeout(() => { label.textContent = original; }, 1400);
}

function bindEvents() {
  document.addEventListener('click', (event) => {
    const clientButton = event.target.closest('[data-client]');
    if (clientButton) {
      state.client = clientButton.dataset.client;
      renderChoices();
      renderConfig();
    }
    const regionButton = event.target.closest('[data-region]');
    if (regionButton) {
      state.region = regionButton.dataset.region;
      renderChoices();
      renderConfig();
    }
  });

  document.getElementById('adblockToggle').addEventListener('change', (event) => {
    state.adblock = event.target.checked;
    renderConfig();
  });

  document.getElementById('copyConfigButton').addEventListener('click', (event) => copyText(buildConfig(), event.currentTarget));
  document.getElementById('copyLinkButton').addEventListener('click', (event) => copyText(document.getElementById('downloadLink').href, event.currentTarget));
}

renderChoices();
bindEvents();
renderConfig();
